# Los nombres de variables con más de una palabra pueden ser difíciles de leer. Existen varias técnicas para hacerlos más legibles:

# Camel Case
# Cada palabra, excepto la primera, empieza con mayúscula:
nombreDeMiVariable = "Sergie Code"

# Pascal Case
# Cada palabra empieza con mayúscula:
NombreDeMiVariable = "Sergie Code"

# Snake Case
# Cada palabra está separada por un guión bajo:
nombre_de_mi_variable = "Sergie Code"



